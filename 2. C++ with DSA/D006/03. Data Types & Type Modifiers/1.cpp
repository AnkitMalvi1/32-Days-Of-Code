#include <iostream>
using namespace std;

int main()
{
    int a;  // declaration
    a = 12; // initialization
    cout << sizeof(a) << endl;
    float b;
    cout << sizeof(b) << endl;
    char c;
    cout << sizeof(c) << endl;
    bool d;
    cout << sizeof(d) << endl;
    short int si;
    cout << sizeof(si) << endl;
    long int li;
    cout << sizeof(li);

    return 0;
}